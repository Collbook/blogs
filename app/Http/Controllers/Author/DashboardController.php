     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = Auth::user();
        $posts = $user->posts;
        $popular_posts = $user->posts()
                        ->withCount('comments')
                        ->withCount('favorite_to_users')
                        ->orderBy('view_count','desc')
                        ->orderBy('comments_count')
                        ->orderBy('favorite_to_users_count')
                        ->take(5)->get();
        $total_pending_posts = $posts->where('is_approved',false)->count();
        $total_post          = $posts->count();
        $all_views = $posts->sum('view_count');
        //dd($popular_posts);
        return view('author.dashboard',compact('posts','popular_posts','total_post','total_pending_posts','all_views'));
    }

